    func countWords(data: String, dict: Dictionary<String,Int>) {
        let array = data.components(separatedBy: [" ", "\n", ".", ",", "'", "-" ])
        var frequencyDict = dict
        
        for word in array {
            if let oldFreq = frequencyDict[word] {
